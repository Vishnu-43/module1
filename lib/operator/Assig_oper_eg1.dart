main(){
  print("Example of Assignment operator");
  var num1=10;
  var num2=5;
  num1+=num2;
  print("num1+=num2=${num1}");
  num1-=num2;
  print("num1-=num2=${num1}");
  num1*=num2;
  print("num1*=num2=${num1}");
  num1~/=num2;
  print("num1~/=num2=${num1}");
  num1%=num2;
  print("num1%=num2=${num1}");

}